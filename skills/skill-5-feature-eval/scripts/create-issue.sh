#!/bin/bash
set -euo pipefail

# 从 eval-doc 提取标题和描述，创建 GitHub issue。
# 需要 gh CLI 并已登录。

DRY_RUN=false
EVAL_DOC=""
REPO=""
LABELS=""

usage() {
    cat <<EOF
Usage: $(basename "$0") --eval-doc <path> --repo <owner/repo> [--labels <labels>] \\
       [--dry-run] [--help]

Create a GitHub issue from an eval-doc file.

Required:
  --eval-doc <path>      Path to the eval-doc markdown file
  --repo <owner/repo>    GitHub repository (e.g. ezagent42/zchat)

Optional:
  --labels <labels>       Comma-separated labels (e.g. "bug,P0")
  --dry-run               Show what would be done without making changes
  --help                  Show this help message

Output: prints the created issue URL to stdout.

Prerequisites:
  - gh CLI installed and authenticated (gh auth status)
  - eval-doc must have YAML frontmatter with 'feature' and 'mode' fields
EOF
    exit 0
}

while [[ $# -gt 0 ]]; do
    case "$1" in
        --eval-doc) EVAL_DOC="$2"; shift 2 ;;
        --repo) REPO="$2"; shift 2 ;;
        --labels) LABELS="$2"; shift 2 ;;
        --dry-run) DRY_RUN=true; shift ;;
        --help) usage ;;
        *) echo "Error: unknown option '$1'. Use --help for usage." >&2; exit 1 ;;
    esac
done

# 参数验证
if [[ -z "$EVAL_DOC" ]]; then
    echo "Error: --eval-doc is required." >&2
    exit 1
fi

if [[ -z "$REPO" ]]; then
    echo "Error: --repo is required." >&2
    exit 1
fi

if [[ ! -f "$EVAL_DOC" ]]; then
    echo "Error: eval-doc not found at '$EVAL_DOC'." >&2
    exit 1
fi

# 检查 gh CLI
if ! command -v gh &> /dev/null; then
    echo "Error: gh CLI not found. Install from https://cli.github.com/" >&2
    exit 1
fi

# 从 frontmatter 提取字段
extract_frontmatter() {
    local field="$1"
    local file="$2"
    python3 -c "
import sys
field = sys.argv[1]
fname = sys.argv[2]
in_fm = False
for line in open(fname):
    line = line.strip()
    if line == '---':
        if in_fm:
            break
        in_fm = True
        continue
    if in_fm and line.startswith(field + ':'):
        val = line.split(':', 1)[1].strip().strip('\"').strip(\"'\")
        print(val)
        break
" "$field" "$file"
}

FEATURE=$(extract_frontmatter "feature" "$EVAL_DOC")
MODE=$(extract_frontmatter "mode" "$EVAL_DOC")
SUBMITTER=$(extract_frontmatter "submitter" "$EVAL_DOC")

if [[ -z "$FEATURE" ]]; then
    echo "Error: could not extract 'feature' from eval-doc frontmatter." >&2
    exit 1
fi

# 构建 issue 标题
if [[ "$MODE" == "verify" ]]; then
    TITLE="[Verify] $FEATURE"
elif [[ "$MODE" == "simulate" ]]; then
    TITLE="[Simulate] $FEATURE"
else
    TITLE="[Eval] $FEATURE"
fi

# 构建 issue body：提取 frontmatter 之后的全部内容
BODY=$(python3 -c "
import sys
fname = sys.argv[1]
in_fm = False
fm_closed = False
lines = []
for line in open(fname):
    stripped = line.strip()
    if stripped == '---':
        if not in_fm:
            in_fm = True
            continue
        else:
            fm_closed = True
            continue
    if fm_closed:
        lines.append(line.rstrip())
# 去掉开头的空行
while lines and not lines[0]:
    lines.pop(0)
print('\n'.join(lines))
" "$EVAL_DOC")

# 追加元信息
BODY="${BODY}

---
*Generated by feature-eval (skill-5) | Submitter: ${SUBMITTER:-unknown} | Mode: ${MODE:-unknown}*"

if $DRY_RUN; then
    echo "[dry-run] Would create GitHub issue:"
    echo "  Repo: $REPO"
    echo "  Title: $TITLE"
    echo "  Labels: ${LABELS:-none}"
    echo "  Body length: ${#BODY} chars"
    echo "[dry-run] Would run: gh issue create --repo $REPO --title \"$TITLE\" --body <body>"
    exit 0
fi

# 创建 issue
LABEL_ARGS=()
if [[ -n "$LABELS" ]]; then
    LABEL_ARGS=(--label "$LABELS")
fi

ISSUE_URL=$(gh issue create \
    --repo "$REPO" \
    --title "$TITLE" \
    --body "$BODY" \
    "${LABEL_ARGS[@]}" \
    2>&1)

echo "$ISSUE_URL"
